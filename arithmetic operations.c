#include<stdio.h>
void main()
{
	int a,b,c,d,e,f,g,h;
	a=10;
	b=2;
	c=a+b;
	d=a-b;
	e=a*b;
	f=a/b;
	g=a%b;
	h=c+d+e+f+g;
	printf("sum of 10 and 2 is %d\n",c);
	printf("difference of 10 and 2 is %d\n",d);
	printf("product of 10 and 2 is %d\n",e);
	printf("quotient of 10 and 2 is %d\n",f);
	printf("remainder of 10 and 2 is %d\n",g);
	printf("sum of all arithmetic operations  is %d\n",h);
	
}
